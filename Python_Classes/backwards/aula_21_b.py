def somar(a, b, c):
    """
    -> Faz a soma de três valores e mostra o resultado na tela.
    :param a: o primeiro valor
    :param b: o segundo valor
    :param c: o terceiro valor
    Função criada por Fabri_Junin
    """
    s = a + b + c
    print(f'A soma vale {s}')

somar(3, 2, 0)
somar(4, 1, 2)
