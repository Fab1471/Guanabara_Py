a = (2, 5, 4)
b = (5, 8, 1, 2)
c = b + a
print(c) # mostra a Tupla, neste caso uma após a outra fazendo uma junção. .
print(len(c)) # mostra q quantidade de termos dentro da Tupla
print(c.count(5)) # qts vezes está aparecendo o número 5 dentro de c?
print(c.index(8)) # em q posição está o 8
# o index pega a primeira ocorrência de algo. .
print(c.index(5, 1)) # aqui ele vai começar na posição 1 e ignorar o primeiro 5 q tá na posição 0. .
